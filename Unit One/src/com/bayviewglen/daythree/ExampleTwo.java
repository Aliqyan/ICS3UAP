package com.bayviewglen.daythree;

public class ExampleTwo {

	public static void main(String[] args) {
		int numberOne, numberTwo; // notice the naming convention for variables (camel case)
		int sum;
		
		numberOne = 7;			//number one is assigned 7
		numberTwo = 12;
		
		sum = numberOne + numberTwo;
		
		System.out.println(sum); // display the value of sum
		
		System.out.println(numberOne + "+" + numberTwo + "=" + sum); //using string concatenation 
		 
		//String is a sequence of letters
		
		System.out.println(numberTwo + numberOne + "+" + numberTwo + "=" + sum);
				
		
	}

}
